function star1(n){
	var star = document.getElementsByClassName("Star1");
	var stars = star[0].getElementsByTagName("a");
	for(i = 0; i < 5; i++){
		stars[i].className = stars[i].className.replace(" on","")
	}
	for(i = 0; i < n; i++){
		stars[i].className +=" on";
	}

}
